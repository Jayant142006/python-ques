"""
Q8 Write a program to check and print whether the alphabet is a vowel or not.
"""

a = input("enter an alphabet: ")
if((a=="a")or(a=="e")or(a=="i")or(a=="o")or(a=="u")):
    print("its a vowel ")
elif((a=="A")or(a=="E")or(a=="I")or(a=="O")or(a=="U")):
    print("its a vowel: ")
else:
    print("not a vowel")
